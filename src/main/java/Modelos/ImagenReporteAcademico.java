public class ImagenReporteAcademico {
    private String nombreP;
    private String apellidopP;
    private String apellidomP;
    private String correo;
    private String nombremateria;
    private String periodo;
    private String semanafiscal;
    private String actual;
    
    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getActual() {
        return actual;
    }

    public void setActual(String actual) {
        this.actual = actual;
    }
    

    public String getNombreP() {
        return nombreP;
    }

    public void setNombreP(String nombreP) {
        this.nombreP = nombreP;
    }

    public String getApellidopP() {
        return apellidopP;
    }

    public void setApellidopP(String apellidopP) {
        this.apellidopP = apellidopP;
    }

    public String getApellidomP() {
        return apellidomP;
    }

    public void setApellidomP(String apellidomP) {
        this.apellidomP = apellidomP;
    }

    public String getNombremateria() {
        return nombremateria;
    }

    public void setNombremateria(String nombremateria) {
        this.nombremateria = nombremateria;
    }

    public String getPeriodo() {
        return periodo;
    }

    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }

    public String getSemanafiscal() {
        return semanafiscal;
    }

    public void setSemanafiscal(String semanafiscal) {
        this.semanafiscal = semanafiscal;
    }
    
    
    
}
